// Helper function to get service by slug
export function getServiceBySlug(slug: string): ServiceDetail | undefined {
  return DETAILED_SERVICES[slug];
}

// Get all services as array
export function getAllServices(): ServiceDetail[] {
  return Object.values(DETAILED_SERVICES);
}

// Get services by category
export function getServicesByCategory(category: ServiceDetail['category']): ServiceDetail[] {
  return getAllServices().filter(service => service.category === category);
}

// Get related services
export function getRelatedServices(serviceSlug: string): ServiceDetail[] {
  const service = getServiceBySlug(serviceSlug);
  if (!service) return [];

  return service.relatedServices
    .map(slug => getServiceBySlug(slug))
    .filter((s): s is ServiceDetail => s !== undefined);
}
